/**
 * Generates lib/offline-regions.ts: for each user-facing region, the list of
 * self-hosted (GCS) photo URLs for species that occur there, so the offline
 * downloader can cache one region at a time instead of the full ~1.2 GB set.
 *
 *   node --experimental-strip-types scripts/build-offline-regions.mjs
 *
 * Re-run after the catalog, galleries, or local-images change.
 */
import { writeFile } from "node:fs/promises";
import path from "node:path";
import { PNW_CATALOG } from "../lib/species-catalog.ts";
import { SPECIES_GALLERY } from "../lib/species-gallery.ts";
import { SPECIES_IMAGES } from "../lib/species-images.ts";
import { LOCAL_IMAGES } from "../lib/local-images.ts";
import { REGIONS } from "../lib/regions.ts";

const ROOT = path.resolve(import.meta.dirname, "..");
const OUT = path.join(ROOT, "lib/offline-regions.ts");

/** Self-hosted (GCS) photo URLs for one species — thumbnails + full. */
function photoUrls(s) {
  const urls = new Set();
  const add = (remote) => { if (remote) urls.add(LOCAL_IMAGES[remote] ?? remote); };
  const lead = SPECIES_IMAGES[s.id];
  if (lead) { add(lead.thumb); add(lead.url); }
  for (const g of SPECIES_GALLERY[s.id] ?? []) { add(g.thumb); add(g.url); }
  return urls;
}

function main() {
  const groups = [];
  for (const r of REGIONS) {
    if (!r.terms) continue; // skip "all"
    const urls = new Set();
    for (const s of PNW_CATALOG) {
      if (!s.regionsPNW.some((reg) => r.terms.some((t) => reg.toLowerCase().includes(t)))) continue;
      for (const u of photoUrls(s)) urls.add(u);
    }
    groups.push({ id: r.id, label: r.label, urls: [...urls].sort() });
  }

  const file = `// AUTO-GENERATED by scripts/build-offline-regions.mjs. Do not edit by hand.
// Photo URLs per region for the opt-in offline downloader.

export interface OfflineRegionGroup {
  id: string;
  label: string;
  urls: string[];
}

export const OFFLINE_REGION_GROUPS: OfflineRegionGroup[] = ${JSON.stringify(groups, null, 2)};
`;
  return writeFile(OUT, file).then(() => {
    console.log(`wrote ${path.relative(ROOT, OUT)}:`);
    for (const g of groups) console.log(`  ${g.id.padEnd(18)} ${String(g.urls.length).padStart(4)} photos`);
  });
}

main().catch((e) => { console.error(e); process.exit(1); });
